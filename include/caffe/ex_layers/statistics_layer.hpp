#ifndef CAFFE_STATISTICS_LAYER_HPP_
#define CAFFE_STATISTICS_LAYER_HPP_

#include <vector>

#include "caffe/blob.hpp"
#include "caffe/layer.hpp"
#include "caffe/proto/caffe.pb.h"

#include "caffe/layers/loss_layer.hpp"
// 统计层 
// 计算多个任务的分类精度
namespace caffe {

/**
 * @brief Computes the classification accuracy for a one-of-many
 *        classification task. 
 */
template <typename Dtype>
class StatisticLayer : public Layer<Dtype> {
 public:
  /**
   * @param param provides StatisticParameter accuracy_param,
   *     with StatisticLayer options:
   *   - top_k (\b optional, default 1).
   *     Sets the maximum rank @f$ k @f$ at which a prediction is considered
   *     correct.  For example, if @f$ k = 5 @f$, a prediction is counted
   *     correct if the correct label is among the top 5 predicted labels.
   */
  explicit StatisticLayer(const LayerParameter& param)
      : Layer<Dtype>(param) {}
  virtual void LayerSetUp(const vector<Blob<Dtype>*>& bottom,
      const vector<Blob<Dtype>*>& top) { current_iter = 0; }
  virtual void Reshape(const vector<Blob<Dtype>*>& bottom,
      const vector<Blob<Dtype>*>& top);

  virtual inline const char* type() const { return "Statistic"; }
  virtual inline int ExactNumBottomBlobs() const { return 1; } //1个bottom blob

  // If there are two top blobs, then the second blob will contain  
  // accuracies per class.
  // 如果包括两个top blob，第二个blob包含每个类的精度
  virtual inline int MinTopBlobs() const { return 1; }
  virtual inline int MaxTopBlos() const { return 1; }

 protected:
  virtual void Forward_cpu(const vector<Blob<Dtype>*>& bottom,
      const vector<Blob<Dtype>*>& top);


  /// @brief Not implemented -- StatisticLayer cannot be used as a loss.
  virtual void Backward_cpu(const vector<Blob<Dtype>*>& top,
      const vector<bool>& propagate_down, const vector<Blob<Dtype>*>& bottom) {
    for (int i = 0; i < propagate_down.size(); ++i) {
      if (propagate_down[i]) { NOT_IMPLEMENTED; }
    }
  }
  int current_iter;

};

}  // namespace caffe 

#endif  // CAFFE_STATISTICS_LAYER_HPP_
